    final controller = TextEditingController(text: _usageRulesEditorSeed());

    await showModalBottomSheet<void>(
      context: context,
      isScrollControlled: true,
      showDragHandle: true,
      builder: (context) {
        final viewInsets = MediaQuery.viewInsetsOf(context);
        return Padding(
          padding: EdgeInsets.fromLTRB(20, 12, 20, 24 + viewInsets.bottom),
          child: Column(
            mainAxisSize: MainAxisSize.min,
            crossAxisAlignment: CrossAxisAlignment.start,
            children: <Widget>[
              const Text(
                'Reglas de uso del bot',
                style: TextStyle(
                  color: Color(0xFF0F172A),
                  fontSize: 20,
                  fontWeight: FontWeight.w800,
                ),
              ),
              const SizedBox(height: 10),
              TextField(
                controller: controller,
                maxLines: 12,
                decoration: const InputDecoration(),
              ),
              const SizedBox(height: 12),
              const Text(
                'Guia por defecto: ubicacion, cuentas, horario y contacto solo cuando el cliente lo pida.',
                style: TextStyle(
                  color: Color(0xFF64748B),
                  fontSize: 12,
                  height: 1.4,
                ),
              ),
              const SizedBox(height: 18),
              Wrap(
                spacing: 12,
                runSpacing: 12,
                children: <Widget>[
                  TextButton(
                    onPressed: () {
                      controller.text = _prettyJson(_defaultUsageRulesTemplate);
                    },
                    child: const Text('Usar guia por defecto'),
                  ),
                  ElevatedButton(
                    onPressed: () {
                      _usageRulesController.text = controller.text.trim();
                      Navigator.of(context).pop();
                    },
                    child: const Text('Aplicar'),
                  ),
                  OutlinedButton(
                    onPressed: () => Navigator.of(context).pop(),
                    child: const Text('Cerrar'),
                  ),
                ],
              ),
            ],
          ),
        );
      },
    );

    controller.dispose();
  }

  void _addBankAccount() {
    setState(() {
      _bankAccounts.add(_BankAccountFormRow.empty());
    });
  }

  void _removeBankAccount(_BankAccountFormRow row) {
    if (_bankAccounts.length == 1) {
      row.clear();
      setState(() {});
      return;
    }

    setState(() {
      _bankAccounts.remove(row);
      row.dispose();
    });
  }

  void _addImage() {
    setState(() {
      _images.add(_ImageFormRow.empty());
    });
  }

  Future<void> _uploadImageForRow(_ImageFormRow row) async {
    setState(() {
      _uploadingImages.add(row);
    });

    try {
      final uploadedUrl = await _pickAndUploadImage(
        titlePrefix: _companyNameController.text.trim().isEmpty
            ? 'Imagen empresa'
            : 'Imagen ${_companyNameController.text.trim()}',
      );
      if (uploadedUrl == null) {
        return;
      }

      row.urlController.text = uploadedUrl;
      if (!mounted) {
        return;
      }

      _showMessage('Imagen cargada correctamente.');
    } catch (error) {
      if (!mounted) {
        return;
      }

      _showMessage(error.toString().replaceFirst('Exception: ', ''), isError: true);
    } finally {
      if (mounted) {
        setState(() {
          _uploadingImages.remove(row);
        });
      }
    }
  }

  Future<void> _uploadBankAccountImage(_BankAccountFormRow row) async {
    setState(() {
      _uploadingBankImages.add(row);
    });

    try {
      final uploadedUrl = await _pickAndUploadImage(
        titlePrefix: row.bankController.text.trim().isEmpty
            ? 'Soporte cuenta bancaria'
            : 'Soporte ${row.bankController.text.trim()}',
      );
      if (uploadedUrl == null) {
        return;
      }

      row.imageController.text = uploadedUrl;
      if (!mounted) {
        return;
      }

      _showMessage('Imagen de cuenta cargada correctamente.');
    } catch (error) {
      if (!mounted) {
        return;
      }

      _showMessage(error.toString().replaceFirst('Exception: ', ''), isError: true);
    } finally {
      if (mounted) {
        setState(() {
          _uploadingBankImages.remove(row);
        });
      }
    }
  }

  void _removeImage(_ImageFormRow row) {
    if (_images.length == 1) {
      row.clear();
      setState(() {});
      return;
    }

    setState(() {
      _images.remove(row);
      row.dispose();
    });
  }

  void _disposeBankAccounts() {
    for (final item in _bankAccounts) {
      item.dispose();
    }
    _bankAccounts.clear();
  }

  void _disposeImages() {
    for (final item in _images) {
      item.dispose();
    }
    _images.clear();
  }

  void _syncMapsLinkPreview() {
    final generated = _buildGeneratedMapsLinkFromCoordinates();
    final current = _mapsLinkController.text.trim();

    if (generated == null) {
      if (current.isEmpty || current == (_lastAutoGeneratedMapsLink ?? '')) {
        _mapsLinkController.clear();
        _lastAutoGeneratedMapsLink = null;
      }
      return;
    }

    if (current.isEmpty || current == (_lastAutoGeneratedMapsLink ?? '')) {
      _mapsLinkController.text = generated;
      _lastAutoGeneratedMapsLink = generated;
    }
  }

  String? _buildGeneratedMapsLinkFromCoordinates() {
    final latitude = double.tryParse(_latitudeController.text.trim());
    final longitude = double.tryParse(_longitudeController.text.trim());

    if (latitude == null || longitude == null) {
      return null;
    }

    return 'https://www.google.com/maps?q=$latitude,$longitude';
  }

  Future<String?> _pickAndUploadImage({required String titlePrefix}) async {
    final result = await FilePicker.platform.pickFiles(
      allowMultiple: false,
      withData: true,
      type: FileType.custom,
      allowedExtensions: const <String>['png', 'jpg', 'jpeg', 'webp', 'svg'],
    );

    final file = result?.files.single;
    if (file == null || file.bytes == null) {
      return null;
    }

    final extension = file.extension?.toLowerCase();
    if (file.size > 20 * 1024 * 1024) {
      throw Exception('La imagen supera el limite de 20MB.');
    }

    final uploaded = await widget.apiService.uploadMedia(
      fileBytes: file.bytes!,
      fileName: file.name,
      contentType: extension == 'png'
          ? 'image/png'
          : extension == 'webp'
              ? 'image/webp'
              : extension == 'svg'
                  ? 'image/svg+xml'
                  : 'image/jpeg',
      title: titlePrefix,
      description: 'Imagen asociada a informacion empresarial',
    );

    return uploaded.fileUrl;
  }

  Map<String, dynamic> _decodeJsonMap(
    String rawValue, {
    required String fieldName,
  }) {
    final trimmed = rawValue.trim();
    if (trimmed.isEmpty) {
      return <String, dynamic>{};
    }

    final decoded = jsonDecode(trimmed);
    if (decoded is! Map<String, dynamic>) {
      throw Exception('$fieldName debe ser un JSON tipo objeto.');
    }

    return decoded;
  }

  double? _parseCoordinate(String rawValue, {required String fieldName}) {
    final trimmed = rawValue.trim();
    if (trimmed.isEmpty) {
      return null;
    }

    final parsed = double.tryParse(trimmed);
    if (parsed == null) {
      throw Exception('$fieldName no es valida.');
    }

    return parsed;
  }

  String _prettyJson(Map<String, dynamic> value) {
    if (value.isEmpty) {
      return '{}';
    }

    return const JsonEncoder.withIndent('  ').convert(value);
  }

  void _showMessage(String message, {bool isError = false}) {
    final messenger = ScaffoldMessenger.of(context);
    messenger.hideCurrentSnackBar();
    messenger.showSnackBar(
      SnackBar(
        content: Text(message),
        backgroundColor: isError ? const Color(0xFF9F1239) : const Color(0xFF166534),
      ),
    );
  }

  void _scrollToTop() {
    WidgetsBinding.instance.addPostFrameCallback((_) {
      if (!mounted) {
        return;
      }

      final position = Scrollable.maybeOf(context)?.position;
      if (position == null) {
        return;
      }

      position.jumpTo(0);
    });
  }

  String _usageRulesEditorSeed() {
    final current = _usageRulesController.text.trim();
    if (current.isEmpty || current == '{}') {
      return _prettyJson(_defaultUsageRulesTemplate);
    }
    return current;
  }

  String _sectionTitle(_CompanySection section) {
    switch (section) {
      case _CompanySection.basic:
        return 'Datos basicos';
      case _CompanySection.contact:
        return 'Contacto';
      case _CompanySection.location:
        return 'Ubicacion';
      case _CompanySection.schedule:
        return 'Horario';
      case _CompanySection.bankAccounts:
        return 'Cuentas bancarias';
      case _CompanySection.images:
        return 'Imagenes';
    }
  }

  String _sectionDescription(_CompanySection section) {
    switch (section) {
      case _CompanySection.basic:
        return 'Nombre y descripcion comercial.';
      case _CompanySection.contact:
        return 'Telefono y WhatsApp.';
      case _CompanySection.location:
        return 'Direccion, GPS y Google Maps.';
      case _CompanySection.schedule:
        return 'Horarios disponibles para el bot.';
      case _CompanySection.bankAccounts:
        return 'Cuentas para pagos y referencias.';
      case _CompanySection.images:
        return 'Imagenes de apoyo o de marca.';
    }
  }

  IconData _sectionIcon(_CompanySection section) {
    switch (section) {
      case _CompanySection.basic:
        return Icons.business_rounded;
      case _CompanySection.contact:
        return Icons.phone_in_talk_rounded;
      case _CompanySection.location:
        return Icons.place_rounded;
      case _CompanySection.schedule:
        return Icons.schedule_rounded;
      case _CompanySection.bankAccounts:
        return Icons.account_balance_rounded;
      case _CompanySection.images:
        return Icons.photo_library_rounded;
    }
  }

  int _nonEmptyBankAccountsCount() {
    return _bankAccounts.where((item) => item.toJson().isNotEmpty).length;
  }

  int _nonEmptyImagesCount() {
    return _images.where((item) => item.toJson().isNotEmpty).length;
  }

  String _sectionStatus(_CompanySection section) {
    switch (section) {
      case _CompanySection.basic:
        return _companyNameController.text.trim().isEmpty ? 'Pendiente' : 'Listo';
      case _CompanySection.contact:
        return _phoneController.text.trim().isEmpty &&
                _whatsappController.text.trim().isEmpty
            ? 'Pendiente'
            : 'Listo';
      case _CompanySection.location:
        return _addressController.text.trim().isEmpty &&
                _mapsLinkController.text.trim().isEmpty
            ? 'Pendiente'
            : 'Listo';
      case _CompanySection.schedule:
        return _workingHoursController.text.trim() == '{}'
            ? 'Pendiente'
            : 'Listo';
      case _CompanySection.bankAccounts:
        final count = _nonEmptyBankAccountsCount();
        return count == 0 ? 'Pendiente' : '$count cuenta${count == 1 ? '' : 's'}';
      case _CompanySection.images:
        final count = _nonEmptyImagesCount();
        return count == 0 ? 'Pendiente' : '$count imagen${count == 1 ? '' : 'es'}';
    }
  }

  List<_CompanyMenuItemData> _menuItems() {
    return <_CompanyMenuItemData>[
      for (final section in _CompanySection.values)
        _CompanyMenuItemData(
          section: section,
          title: _sectionTitle(section),
          description: _sectionDescription(section),
          status: _sectionStatus(section),
          icon: _sectionIcon(section),
        ),
    ];
  }

  Widget _buildMenuView(bool isBusy) {
    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: <Widget>[
        if (_isLoading) ...<Widget>[
          const SizedBox(height: 4),
          const LinearProgressIndicator(minHeight: 2),
        ],
        if (_loadError != null) ...<Widget>[
          const SizedBox(height: 12),
          _CompanyMessageLine(
            message: _loadError!,
            color: const Color(0xFF9F1239),
          ),
        ],
        const SizedBox(height: 18),
        _CompanyMenuList(
          items: _menuItems(),
          enabled: !_isLoading,
          onTap: (_CompanySection section) {
            setState(() {
              _selectedSection = section;
            });
            _notifyMainViewState();
            _scrollToTop();
          },
        ),
      ],
    );
  }

  Widget _buildDetailView(_CompanySection section, bool isBusy) {
    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: <Widget>[
        _CompanyDetailHeader(
          title: _sectionTitle(section),
          subtitle: _sectionDescription(section),
          onBack: () {
            setState(() {
              _selectedSection = null;
            });
            _notifyMainViewState();
            _scrollToTop();
          },
          onReload: isBusy ? null : _loadContext,
        ),
        if (_loadError != null) ...<Widget>[
          const SizedBox(height: 12),
          _CompanyMessageLine(
            message: _loadError!,
            color: const Color(0xFF9F1239),
          ),
        ],
        const SizedBox(height: 18),
        switch (section) {
          _CompanySection.basic => _buildBasicDataSection(isBusy),
          _CompanySection.contact => _buildContactSection(isBusy),
          _CompanySection.location => _buildLocationSection(isBusy),
          _CompanySection.schedule => _buildWorkingHoursSection(isBusy),
          _CompanySection.bankAccounts => _buildBankAccountsSection(isBusy),
          _CompanySection.images => _buildImagesSection(isBusy),
        },
        const SizedBox(height: 24),
        Wrap(
          spacing: 12,
          runSpacing: 12,
          children: <Widget>[
            ElevatedButton(
              onPressed: isBusy ? null : _saveContext,
              child: Text(_isSaving ? 'Guardando...' : 'Guardar cambios'),
            ),
            OutlinedButton(
              onPressed: isBusy
                  ? null
                  : () {
                      setState(() {
                        _selectedSection = null;
                      });
                      _notifyMainViewState();
