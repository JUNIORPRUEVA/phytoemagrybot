class CompanyContextPage extends StatefulWidget {
  const CompanyContextPage({
    super.key,
    required this.apiService,
    required this.onConfigUpdated,
    this.onRequestBack,
    this.onMainViewChanged,
  });

  final ApiService apiService;
  final VoidCallback onConfigUpdated;
  final VoidCallback? onRequestBack;
  final ValueChanged<bool>? onMainViewChanged;

  @override
  State<CompanyContextPage> createState() => _CompanyContextPageState();
}

abstract class CompanyContextPageStateAccess {
  void openUsageRulesEditor();
}

class _CompanyContextPageState extends State<CompanyContextPage>
    implements CompanyContextPageStateAccess {
  static const Map<String, dynamic> _defaultUsageRulesTemplate =
      <String, dynamic>{
        'send_location': 'solo_si_cliente_la_pide',
        'send_bank_accounts': 'solo_si_cliente_quiere_pagar',
        'send_schedule': 'solo_si_cliente_pregunta_horario',
        'send_contact': 'solo_si_cliente_pide_contacto',
      };

  final TextEditingController _companyNameController = TextEditingController();
  final TextEditingController _descriptionController = TextEditingController();
  final TextEditingController _phoneController = TextEditingController();
  final TextEditingController _whatsappController = TextEditingController();
  final TextEditingController _addressController = TextEditingController();
  final TextEditingController _latitudeController = TextEditingController();
  final TextEditingController _longitudeController = TextEditingController();
  final TextEditingController _mapsLinkController = TextEditingController();
  final TextEditingController _workingHoursController = TextEditingController();
  final TextEditingController _usageRulesController = TextEditingController();

  final List<_BankAccountFormRow> _bankAccounts = <_BankAccountFormRow>[];
  final List<_ImageFormRow> _images = <_ImageFormRow>[];

  bool _isLoading = true;
  bool _isSaving = false;
  bool _isLocating = false;
  bool _isUploadingLogo = false;
  final Set<_ImageFormRow> _uploadingImages = <_ImageFormRow>{};
  final Set<_BankAccountFormRow> _uploadingBankImages = <_BankAccountFormRow>{};
  String? _lastAutoGeneratedMapsLink;
  String? _loadError;
  _CompanySection? _selectedSection;
  String _companyLogoUrl = '';
  String _brandingCompanyDetails = '';
  String _brandingCompanyName = '';

  @override
  void initState() {
    super.initState();
    _latitudeController.addListener(_syncMapsLinkPreview);
    _longitudeController.addListener(_syncMapsLinkPreview);
    WidgetsBinding.instance.addPostFrameCallback((_) {
      if (mounted) {
        _notifyMainViewState();
      }
    });
    _loadContext();
  }

  @override
  void didUpdateWidget(covariant CompanyContextPage oldWidget) {
    super.didUpdateWidget(oldWidget);
    if (oldWidget.apiService.baseUrl != widget.apiService.baseUrl) {
      _loadContext();
    }
  }

  @override
  void dispose() {
    _companyNameController.dispose();
    _descriptionController.dispose();
    _phoneController.dispose();
    _whatsappController.dispose();
    _addressController.dispose();
    _latitudeController.dispose();
    _longitudeController.dispose();
    _mapsLinkController.dispose();
    _workingHoursController.dispose();
    _usageRulesController.dispose();
    _disposeBankAccounts();
    _disposeImages();
    super.dispose();
  }

  void _notifyMainViewState() {
    widget.onMainViewChanged?.call(_selectedSection == null);
  }

  Future<void> _loadContext() async {
    setState(() {
      _isLoading = true;
      _loadError = null;
    });

    try {
      final results = await Future.wait<Object>(<Future<Object>>[
        widget.apiService.getCompanyContext(),
        widget.apiService.getConfig(),
      ]);
      final context = results[0] as CompanyContextData;
      final config = results[1] as ClientConfigData;
      _applyContext(context);
      _applyBranding(config);
    } catch (error) {
      _applyContext(CompanyContextData.empty());
      _applyBranding(ClientConfigData.empty());
      if (!mounted) {
        return;
      }

      setState(() {
        _loadError = error.toString().replaceFirst('Exception: ', '');
      });
    } finally {
      if (mounted) {
        setState(() {
          _isLoading = false;
        });
      }
    }
  }

  void _applyBranding(ClientConfigData config) {
    _companyLogoUrl = config.companyLogoUrl;
    _brandingCompanyDetails = config.companyDetails;
    _brandingCompanyName = config.companyName;
  }

  void _applyContext(CompanyContextData context) {
    _companyNameController.text = context.companyName;
    _descriptionController.text = context.description;
    _phoneController.text = context.phone;
    _whatsappController.text = context.whatsapp;
    _addressController.text = context.address;
    _latitudeController.text = context.latitude?.toString() ?? '';
    _longitudeController.text = context.longitude?.toString() ?? '';
    _mapsLinkController.text = context.googleMapsLink;
    _lastAutoGeneratedMapsLink =
      context.googleMapsLink.trim().isNotEmpty &&
        context.googleMapsLink.trim() == _buildGeneratedMapsLinkFromCoordinates()
      ? context.googleMapsLink.trim()
      : null;
    _workingHoursController.text = _prettyJson(context.workingHoursJson);
    _usageRulesController.text = _prettyJson(context.usageRulesJson);

    _disposeBankAccounts();
    _disposeImages();

    for (final item in context.bankAccountsJson) {
      _bankAccounts.add(_BankAccountFormRow.fromData(item));
    }
    for (final item in context.imagesJson) {
      _images.add(_ImageFormRow.fromData(item));
    }

    if (_bankAccounts.isEmpty) {
      _bankAccounts.add(_BankAccountFormRow.empty());
    }
    if (_images.isEmpty) {
      _images.add(_ImageFormRow.empty());
    }

    _syncMapsLinkPreview();
  }

  Future<void> _saveContext() async {
    setState(() {
      _isSaving = true;
    });

    try {
      final workingHours = _decodeJsonMap(
        _workingHoursController.text,
        fieldName: 'Horario',
      );
      final usageRules = _decodeJsonMap(
        _usageRulesController.text,
        fieldName: 'Reglas de uso',
      );
      final latitude = _parseCoordinate(
        _latitudeController.text,
        fieldName: 'Latitud',
      );
      final longitude = _parseCoordinate(
        _longitudeController.text,
        fieldName: 'Longitud',
      );
      final bankAccounts = _bankAccounts
          .map((item) => item.toJson())
          .where((item) => item.isNotEmpty)
          .toList();
      final images = _images
          .map((item) => item.toJson())
          .where((item) => item.isNotEmpty)
          .toList();
      final normalizedCompanyName = _companyNameController.text.trim();

      final saved = await widget.apiService.saveCompanyContext(
        companyName: normalizedCompanyName,
        description: _descriptionController.text.trim(),
        phone: _phoneController.text.trim(),
        whatsapp: _whatsappController.text.trim(),
        address: _addressController.text.trim(),
        googleMapsLink: _mapsLinkController.text.trim(),
        latitude: latitude,
        longitude: longitude,
        workingHoursJson: workingHours,
        bankAccountsJson: bankAccounts,
        imagesJson: images,
        usageRulesJson: usageRules,
      );
      final brandingConfig = await widget.apiService.saveBrandingSettings(
        companyName: normalizedCompanyName.isEmpty
            ? _brandingCompanyName
            : normalizedCompanyName,
        companyDetails: _brandingCompanyDetails,
        companyLogoUrl: _companyLogoUrl.trim(),
      );

      if (!mounted) {
        return;
      }

      _applyContext(saved);
      _applyBranding(brandingConfig);
      widget.onConfigUpdated();
      _showMessage('Informacion de la empresa guardada.');
    } catch (error) {
      if (!mounted) {
        return;
      }

      _showMessage(error.toString().replaceFirst('Exception: ', ''), isError: true);
    } finally {
      if (mounted) {
        setState(() {
          _isSaving = false;
        });
      }
    }
  }

  Future<void> _pickAndUploadLogo() async {
    setState(() {
      _isUploadingLogo = true;
    });

    try {
      final result = await FilePicker.platform.pickFiles(
        allowMultiple: false,
        withData: true,
        type: FileType.custom,
        allowedExtensions: const <String>['png', 'jpg', 'jpeg', 'webp', 'svg'],
      );

      final file = result?.files.single;
      if (file == null || file.bytes == null) {
        return;
      }

      final extension = file.extension?.toLowerCase();
      final uploaded = await widget.apiService.uploadMedia(
        fileBytes: file.bytes!,
        fileName: file.name,
        contentType: extension == 'png'
            ? 'image/png'
            : extension == 'webp'
                ? 'image/webp'
                : extension == 'svg'
                    ? 'image/svg+xml'
                    : 'image/jpeg',
        title: 'Logo ${_companyNameController.text.trim().isEmpty ? 'empresa' : _companyNameController.text.trim()}',
        description: 'Logo corporativo',
      );

      if (!mounted) {
        return;
      }

      setState(() {
        _companyLogoUrl = uploaded.fileUrl;
      });
      widget.onConfigUpdated();
      _showMessage('Logo cargado. Guarda cambios para persistirlo.');
    } catch (error) {
      if (!mounted) {
        return;
      }

      _showMessage(error.toString().replaceFirst('Exception: ', ''), isError: true);
    } finally {
      if (mounted) {
        setState(() {
          _isUploadingLogo = false;
        });
      }
    }
  }

  Future<void> _captureLocation() async {
    setState(() {
      _isLocating = true;
    });

    try {
      final servicesEnabled = await Geolocator.isLocationServiceEnabled();
      if (!servicesEnabled) {
        throw Exception('Activa la ubicacion del dispositivo para obtener el GPS.');
      }

      var permission = await Geolocator.checkPermission();
      if (permission == LocationPermission.denied) {
        permission = await Geolocator.requestPermission();
      }

      if (permission == LocationPermission.denied ||
          permission == LocationPermission.deniedForever) {
        throw Exception('No se concedio permiso para leer la ubicacion.');
      }

      final position = await Geolocator.getCurrentPosition();
      _latitudeController.text = position.latitude.toStringAsFixed(6);
      _longitudeController.text = position.longitude.toStringAsFixed(6);
      _syncMapsLinkPreview();

      if (!mounted) {
        return;
      }

      _showMessage('Ubicacion obtenida. Guarda para persistir el enlace de mapa.');
    } catch (error) {
      if (!mounted) {
        return;
      }

      _showMessage(error.toString().replaceFirst('Exception: ', ''), isError: true);
    } finally {
      if (mounted) {
        setState(() {
          _isLocating = false;
        });
      }
    }
  }

  Future<void> _openMapsLink() async {
    final link = _mapsLinkController.text.trim();
    if (link.isEmpty) {
      _showMessage('Todavia no hay enlace de mapa disponible.', isError: true);
      return;
    }

    final launched = await launchUrl(Uri.parse(link));
    if (!launched && mounted) {
      _showMessage('No se pudo abrir Google Maps.', isError: true);
    }
  }

  @override
  void openUsageRulesEditor() {
    _showUsageRulesSheet();
  }

